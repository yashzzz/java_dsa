public class Relational_operator {
    public static void main(String args[]){
        int a =10;
        int b = 5;
        System.out.println("checking the equality " + (a==b));
        System.out.println("checking the not quality " + (a!=b));
        System.out.println("checking the greater " + (a>b));
        System.out.println("checking the less " + (a<b));
    }
}
