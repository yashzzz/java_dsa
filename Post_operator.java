public class Post_operator {
    public static void main(String args[]){
        int a = 10;
        int b = a++; //post increment operator
        int c = a--; // post decrement operator
        System.out.println("the value of a = " + a ); 
        System.out.println("value of b = " + b); //value of a first assign to b then increase by one due to post
        System.out.println(" value of c =" + c); //value of a first assign to b  then decrease by one due to post decrement
        }
    
}

    

