public class Pre_operator {
    public static void main(String args[]){
        int a = 10;
        int b = ++a; //pre increment operator
        int c = --a; // pre decrement operator
        System.out.println("the value of a = " + a ); 
        System.out.println("value of b = " + b); //value of b firstly change or increase by one because of pre increment
         System.out.println(" value of c =" + c); //value of c is first decrease by one due to pre decrement
        }
    
}
