public class Swtch {
    public static void main(String args[]){
        int a = 2;
        switch(a){
            case 1:
            {
                System.out.println("apple");
              break;
            }
            case 2:
            {
                System.out.println("mango");
                break;
            }
            default:
            {
                System.out.println("nil");
            }
        }
    }
    
}
